def sigma_matrix2(sig_x, sig_y, theta):
    """Calculate the rotated sigma matrix (two dimensional matrix).
    
    This function computes a 2D covariance matrix for a bivariate Gaussian distribution,
    rotated by the specified angle theta.

    Args:
        sig_x (float): Standard deviation in x direction before rotation
        sig_y (float): Standard deviation in y direction before rotation
        theta (float): Rotation angle in radians

    Returns:
        ndarray: 2x2 rotated covariance matrix
    """
    d_matrix = np.array([[sig_x**2, 0], [0, sig_y**2]])
    u_matrix = np.array([[np.cos(theta), -np.sin(theta)], [np.sin(theta), np.cos(theta)]])
    return np.dot(u_matrix, np.dot(d_matrix, u_matrix.T))


def mesh_grid(kernel_size):
    """Generate a centered mesh grid for kernel computation.
    
    Creates a coordinate grid centered at zero with specified size. This is used
    as the base for computing various kernel functions.

    Args:
        kernel_size (int): Size of the kernel (both width and height)

    Returns:
        tuple: Contains:
            - xy (ndarray): Combined x,y coordinates, shape (kernel_size, kernel_size, 2)
            - xx (ndarray): X coordinates only, shape (kernel_size, kernel_size)
            - yy (ndarray): Y coordinates only, shape (kernel_size, kernel_size)
    """
    ax = np.arange(-kernel_size // 2 + 1., kernel_size // 2 + 1.)
    xx, yy = np.meshgrid(ax, ax)
    xy = np.hstack((xx.reshape((kernel_size * kernel_size, 1)), yy.reshape(kernel_size * kernel_size,
                                                                           1))).reshape(kernel_size, kernel_size, 2)
    return xy, xx, yy


def pdf2(sigma_matrix, grid):
    """Calculate PDF of the bivariate Gaussian distribution.
    
    Computes the probability density function for a bivariate Gaussian distribution
    given a covariance matrix and coordinate grid.

    Args:
        sigma_matrix (ndarray): 2x2 covariance matrix
        grid (ndarray): Coordinate grid generated by mesh_grid(), shape (K, K, 2)

    Returns:
        ndarray: Un-normalized kernel values computed from the PDF
    """
    inverse_sigma = np.linalg.inv(sigma_matrix)
    kernel = np.exp(-0.5 * np.sum(np.dot(grid, inverse_sigma) * grid, 2))
    return kernel


def cdf2(d_matrix, grid):
    """Calculate the CDF of the standard bivariate Gaussian distribution.
    
    Computes the cumulative distribution function for a standard bivariate 
    Gaussian distribution with the given skew matrix. Used for generating
    skewed Gaussian distributions.

    Args:
        d_matrix (ndarray): 2x2 skew matrix
        grid (ndarray): Coordinate grid generated by mesh_grid(), shape (K, K, 2)

    Returns:
        ndarray: CDF values for the skewed distribution
    """
    rv = multivariate_normal([0, 0], [[1, 0], [0, 1]])
    grid = np.dot(grid, d_matrix)
    cdf = rv.cdf(grid)
    return cdf


def bivariate_Gaussian(kernel_size, sig_x, sig_y, theta, grid=None, isotropic=True):
    """Generate a bivariate isotropic or anisotropic Gaussian kernel.

    In the isotropic mode, only `sig_x` is used. `sig_y` and `theta` is ignored.

    Args:
        kernel_size (int):
        sig_x (float):
        sig_y (float):
        theta (float): Radian measurement.
        grid (ndarray, optional): generated by :func:`mesh_grid`,
            with the shape (K, K, 2), K is the kernel size. Default: None
        isotropic (bool):

    Returns:
        kernel (ndarray): normalized kernel.
    """
    if grid is None:
        grid, _, _ = mesh_grid(kernel_size)
    if isotropic:
        sigma_matrix = np.array([[sig_x**2, 0], [0, sig_x**2]])
    else:
        sigma_matrix = sigma_matrix2(sig_x, sig_y, theta)
    kernel = pdf2(sigma_matrix, grid)
    kernel = kernel / np.sum(kernel)
    return kernel


def bivariate_generalized_Gaussian(kernel_size, sig_x, sig_y, theta, beta, grid=None, isotropic=True):
    """Generate a bivariate generalized Gaussian kernel.

    This function generates a bivariate generalized Gaussian kernel that can be used for image blurring.
    The generalized Gaussian distribution allows for heavier or lighter tails compared to the normal
    distribution by adjusting the shape parameter beta.

    Reference paper: "Parameter Estimation For Multivariate Generalized Gaussian Distributions"

    In isotropic mode, the kernel is radially symmetric and only sig_x is used. The sig_y and theta
    parameters are ignored in this case.

    The kernel is computed using the formula:
    K(x,y) = exp(-0.5 * (x^T Σ^(-1) x)^β)
    where Σ is the covariance matrix determined by sig_x, sig_y and theta.

    Args:
        kernel_size (int): Size of the kernel (must be odd)
        sig_x (float): Standard deviation in x direction
        sig_y (float): Standard deviation in y direction (ignored if isotropic=True)
        theta (float): Rotation angle in radians (ignored if isotropic=True)
        beta (float): Shape parameter that controls tail weight. beta=1 gives normal distribution,
                     beta<1 gives heavier tails, beta>1 gives lighter tails
        grid (ndarray, optional): Pre-computed coordinate grid from mesh_grid().
                                Shape (kernel_size, kernel_size, 2). If None, will be generated.
        isotropic (bool): If True, generate isotropic kernel using only sig_x.
                         If False, use anisotropic kernel with all parameters.

    Returns:
        ndarray: Normalized kernel array of shape (kernel_size, kernel_size)
    """
    if grid is None:
        grid, _, _ = mesh_grid(kernel_size)
    if isotropic:
        sigma_matrix = np.array([[sig_x**2, 0], [0, sig_x**2]])
    else:
        sigma_matrix = sigma_matrix2(sig_x, sig_y, theta)
    inverse_sigma = np.linalg.inv(sigma_matrix)
    kernel = np.exp(-0.5 * np.power(np.sum(np.dot(grid, inverse_sigma) * grid, 2), beta))
    kernel = kernel / np.sum(kernel)
    return kernel


def bivariate_plateau(kernel_size, sig_x, sig_y, theta, beta, grid=None, isotropic=True):
    """Generate a plateau-like anisotropic kernel.

    This function generates a kernel with a plateau-like shape using the formula 1/(1 + x^beta).
    The resulting kernel has a flatter top and steeper sides compared to a Gaussian kernel.
    This can be useful for modeling certain types of blur patterns.

    Reference: https://stats.stackexchange.com/questions/203629/is-there-a-plateau-shaped-distribution

    In isotropic mode, the kernel is radially symmetric and only sig_x is used. The sig_y and theta
    parameters are ignored in this case.

    The kernel is computed using the formula:
    K(x,y) = 1 / (1 + (x^T Σ^(-1) x)^β)
    where Σ is the covariance matrix determined by sig_x, sig_y and theta.

    Args:
        kernel_size (int): Size of the kernel (must be odd)
        sig_x (float): Standard deviation in x direction
        sig_y (float): Standard deviation in y direction (ignored if isotropic=True)
        theta (float): Rotation angle in radians (ignored if isotropic=True)
        beta (float): Shape parameter that controls the steepness of the sides.
                     Larger values give steeper transitions from plateau to background.
        grid (ndarray, optional): Pre-computed coordinate grid from mesh_grid().
                                Shape (kernel_size, kernel_size, 2). If None, will be generated.
        isotropic (bool): If True, generate isotropic kernel using only sig_x.
                         If False, use anisotropic kernel with all parameters.

    Returns:
        ndarray: Normalized kernel array of shape (kernel_size, kernel_size)
    """
    if grid is None:
        grid, _, _ = mesh_grid(kernel_size)
    if isotropic:
        sigma_matrix = np.array([[sig_x**2, 0], [0, sig_x**2]])
    else:
        sigma_matrix = sigma_matrix2(sig_x, sig_y, theta)
    inverse_sigma = np.linalg.inv(sigma_matrix)
    kernel = np.reciprocal(np.power(np.sum(np.dot(grid, inverse_sigma) * grid, 2), beta) + 1)
    kernel = kernel / np.sum(kernel)
    return kernel


def random_bivariate_Gaussian(kernel_size,
                              sigma_x_range,
                              sigma_y_range,
                              rotation_range,
                              noise_range=None,
                              isotropic=True):
    """Randomly generate bivariate isotropic or anisotropic Gaussian kernels.

    This function generates random Gaussian blur kernels by sampling parameters from specified ranges.
    The kernels can be either isotropic (radially symmetric) or anisotropic (different spreads in
    different directions).

    In isotropic mode, only sigma_x_range is used and the kernel will be radially symmetric.
    Optional multiplicative noise can be added to introduce irregularities in the kernel.

    Args:
        kernel_size (int): Size of the kernel (must be odd)
        sigma_x_range (tuple): Range for sampling x-direction standard deviation, e.g. (0.6, 5)
        sigma_y_range (tuple): Range for sampling y-direction standard deviation (ignored if isotropic)
        rotation_range (tuple): Range for sampling rotation angle in radians, e.g. (-pi, pi)
                              (ignored if isotropic)
        noise_range (tuple, optional): Range for sampling multiplicative noise values.
                                     Typically (0.75, 1.25). If None, no noise is added.
        isotropic (bool): If True, generate isotropic kernels using only sigma_x_range.
                         If False, generate anisotropic kernels using all parameters.

    Returns:
        ndarray: Normalized random kernel array of shape (kernel_size, kernel_size)

    Raises:
        AssertionError: If kernel_size is even or if any range arguments are invalid
    """
    assert kernel_size % 2 == 1, 'Kernel size must be an odd number.'
    assert sigma_x_range[0] < sigma_x_range[1], 'Wrong sigma_x_range.'
    sigma_x = np.random.uniform(sigma_x_range[0], sigma_x_range[1])
    if isotropic is False:
        assert sigma_y_range[0] < sigma_y_range[1], 'Wrong sigma_y_range.'
        assert rotation_range[0] < rotation_range[1], 'Wrong rotation_range.'
        sigma_y = np.random.uniform(sigma_y_range[0], sigma_y_range[1])
        rotation = np.random.uniform(rotation_range[0], rotation_range[1])
    else:
        sigma_y = sigma_x
        rotation = 0

    kernel = bivariate_Gaussian(kernel_size, sigma_x, sigma_y, rotation, isotropic=isotropic)

    # add multiplicative noise
    if noise_range is not None:
        assert noise_range[0] < noise_range[1], 'Wrong noise range.'
        noise = np.random.uniform(noise_range[0], noise_range[1], size=kernel.shape)
        kernel = kernel * noise
    kernel = kernel / np.sum(kernel)
    return kernel


def random_bivariate_generalized_Gaussian(kernel_size,
                                          sigma_x_range,
                                          sigma_y_range,
                                          rotation_range,
                                          beta_range,
                                          noise_range=None,
                                          isotropic=True):
    """Randomly generate bivariate generalized Gaussian kernels.

    This function generates random generalized Gaussian blur kernels by sampling parameters 
    from specified ranges. The kernels can be either isotropic (radially symmetric) or 
    anisotropic (different spreads in different directions).

    The generalized Gaussian distribution allows for varying tail weight through the beta parameter.
    When beta=2, it reduces to a standard Gaussian. Beta<2 gives heavier tails, while beta>2 gives
    lighter tails.

    In isotropic mode, only sigma_x_range is used and the kernel will be radially symmetric.
    Optional multiplicative noise can be added to introduce irregularities in the kernel.

    Args:
        kernel_size (int): Size of the kernel (must be odd)
        sigma_x_range (tuple): Range for sampling x-direction standard deviation, e.g. (0.6, 5)
        sigma_y_range (tuple): Range for sampling y-direction standard deviation (ignored if isotropic)
        rotation_range (tuple): Range for sampling rotation angle in radians, e.g. (-pi, pi)
                              (ignored if isotropic)
        beta_range (tuple): Range for sampling beta shape parameter, e.g. (0.5, 8).
                          Beta controls tail weight of the distribution.
        noise_range (tuple, optional): Range for sampling multiplicative noise values.
                                     Typically (0.75, 1.25). If None, no noise is added.
        isotropic (bool): If True, generate isotropic kernels using only sigma_x_range.
                         If False, generate anisotropic kernels using all parameters.

    Returns:
        ndarray: Normalized random kernel array of shape (kernel_size, kernel_size)

    Raises:
        AssertionError: If kernel_size is even or if any range arguments are invalid
    """
    assert kernel_size % 2 == 1, 'Kernel size must be an odd number.'
    assert sigma_x_range[0] < sigma_x_range[1], 'Wrong sigma_x_range.'
    sigma_x = np.random.uniform(sigma_x_range[0], sigma_x_range[1])
    if isotropic is False:
        assert sigma_y_range[0] < sigma_y_range[1], 'Wrong sigma_y_range.'
        assert rotation_range[0] < rotation_range[1], 'Wrong rotation_range.'
        sigma_y = np.random.uniform(sigma_y_range[0], sigma_y_range[1])
        rotation = np.random.uniform(rotation_range[0], rotation_range[1])
    else:
        sigma_y = sigma_x
        rotation = 0

    # assume beta_range[0] < 1 < beta_range[1]
    if np.random.uniform() < 0.5:
        beta = np.random.uniform(beta_range[0], 1)
    else:
        beta = np.random.uniform(1, beta_range[1])

    kernel = bivariate_generalized_Gaussian(kernel_size, sigma_x, sigma_y, rotation, beta, isotropic=isotropic)

    # add multiplicative noise
    if noise_range is not None:
        assert noise_range[0] < noise_range[1], 'Wrong noise range.'
        noise = np.random.uniform(noise_range[0], noise_range[1], size=kernel.shape)
        kernel = kernel * noise
    kernel = kernel / np.sum(kernel)
    return kernel


