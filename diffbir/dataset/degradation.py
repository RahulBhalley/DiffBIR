def random_mixed_kernels(kernel_list,
                         kernel_prob,
                         kernel_size=21,
                         sigma_x_range=(0.6, 5),
                         sigma_y_range=(0.6, 5),
                         rotation_range=(-math.pi, math.pi),
                         betag_range=(0.5, 8),
                         betap_range=(0.5, 8),
                         noise_range=None):
    """Randomly generate mixed kernels by sampling from different kernel types.

    This function generates blur kernels by randomly selecting from a list of different kernel types
    according to specified probabilities. This allows modeling of complex blur patterns that may
    occur in real images.

    The supported kernel types include:
    - 'iso': Isotropic Gaussian
    - 'aniso': Anisotropic Gaussian  
    - 'generalized_iso': Isotropic generalized Gaussian
    - 'generalized_aniso': Anisotropic generalized Gaussian
    - 'plateau_iso': Isotropic plateau
    - 'plateau_aniso': Anisotropic plateau

    Args:
        kernel_list (list[str]): List of kernel types to sample from
        kernel_prob (list[float]): Corresponding probability for each kernel type
        kernel_size (int): Size of kernel (must be odd). Default: 21
        sigma_x_range (tuple): Range for x-direction spread, e.g. (0.6, 5). Default: (0.6, 5)
        sigma_y_range (tuple): Range for y-direction spread. Default: (0.6, 5)
        rotation_range (tuple): Range for rotation in radians. Default: (-pi, pi)
        betag_range (tuple): Beta range for generalized Gaussian kernels. Default: (0.5, 8)
        betap_range (tuple): Beta range for plateau kernels. Default: (0.5, 8)
        noise_range (tuple, optional): Range for multiplicative noise. Default: None

    Returns:
        ndarray: Normalized random kernel array of shape (kernel_size, kernel_size)

    Raises:
        ValueError: If kernel_list contains invalid kernel type
    """
    kernel_type = random.choices(kernel_list, kernel_prob)[0]
    if kernel_type == 'iso':
        kernel = random_bivariate_Gaussian(
            kernel_size, sigma_x_range, sigma_y_range, rotation_range, noise_range=noise_range, isotropic=True)
    elif kernel_type == 'aniso':
        kernel = random_bivariate_Gaussian(
            kernel_size, sigma_x_range, sigma_y_range, rotation_range, noise_range=noise_range, isotropic=False)
    elif kernel_type == 'generalized_iso':
        kernel = random_bivariate_generalized_Gaussian(
            kernel_size,
            sigma_x_range,
            sigma_y_range,
            rotation_range,
            betag_range,
            noise_range=noise_range,
            isotropic=True)
    elif kernel_type == 'generalized_aniso':
        kernel = random_bivariate_generalized_Gaussian(
            kernel_size,
            sigma_x_range,
            sigma_y_range,
            rotation_range,
            betag_range,
            noise_range=noise_range,
            isotropic=False)
    elif kernel_type == 'plateau_iso':
        kernel = random_bivariate_plateau(
            kernel_size, sigma_x_range, sigma_y_range, rotation_range, betap_range, noise_range=None, isotropic=True)
    elif kernel_type == 'plateau_aniso':
        kernel = random_bivariate_plateau(
            kernel_size, sigma_x_range, sigma_y_range, rotation_range, betap_range, noise_range=None, isotropic=False)
    return kernel


