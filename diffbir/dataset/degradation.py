def circular_lowpass_kernel(cutoff, kernel_size, pad_to=0):
    """Generate a 2D circular lowpass filter kernel using the sinc function.

    This function creates an ideal circular lowpass filter in the frequency domain
    using the sinc function. The filter is radially symmetric and has a sharp cutoff
    at the specified frequency.

    The implementation uses the Bessel function of first kind (J1) to generate
    the filter kernel. This provides better numerical stability compared to
    direct sinc computation.

    Reference: https://dsp.stackexchange.com/questions/58301/2-d-circularly-symmetric-low-pass-filter

    Args:
        cutoff (float): Cutoff frequency in radians (pi is max)
        kernel_size (int): Size of the kernel (must be odd)
        pad_to (int): Optional size to pad kernel to (must be odd or zero). Default: 0

    Returns:
        ndarray: Normalized lowpass filter kernel of shape (kernel_size, kernel_size)
                or (pad_to, pad_to) if padding is specified

    Raises:
        AssertionError: If kernel_size is even
    """
    assert kernel_size % 2 == 1, 'Kernel size must be an odd number.'
    kernel = np.fromfunction(
        lambda x, y: cutoff * special.j1(cutoff * np.sqrt(
            (x - (kernel_size - 1) / 2)**2 + (y - (kernel_size - 1) / 2)**2)) / (2 * np.pi * np.sqrt(
                (x - (kernel_size - 1) / 2)**2 + (y - (kernel_size - 1) / 2)**2)), [kernel_size, kernel_size])
    kernel[(kernel_size - 1) // 2, (kernel_size - 1) // 2] = cutoff**2 / (4 * np.pi)
    kernel = kernel / np.sum(kernel)
    if pad_to > kernel_size:
        pad_size = (pad_to - kernel_size) // 2
        kernel = np.pad(kernel, ((pad_size, pad_size), (pad_size, pad_size)))
    return kernel

