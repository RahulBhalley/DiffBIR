def generate_gaussian_noise(img, sigma=10, gray_noise=False):
    """Generate Gaussian noise to be added to an image.

    Args:
        img (ndarray): Input image array of shape (h, w, c), range [0, 1], float32.
        sigma (float): Standard deviation of the Gaussian noise, measured in range [0, 255].
            Default: 10.
        gray_noise (bool): If True, generates grayscale noise that is same across all channels.
            If False, generates independent noise per channel. Default: False.

    Returns:
        ndarray: Generated noise array of same shape as input image, range roughly [-sigma/255, sigma/255].
    """
    if gray_noise:
        noise = np.float32(np.random.randn(*(img.shape[0:2]))) * sigma / 255.
        noise = np.expand_dims(noise, axis=2).repeat(3, axis=2)
    else:
        noise = np.float32(np.random.randn(*(img.shape))) * sigma / 255.
    return noise


def add_gaussian_noise(img, sigma=10, clip=True, rounds=False, gray_noise=False):
    """Add Gaussian noise to an image.

    Args:
        img (ndarray): Input image array of shape (h, w, c), range [0, 1], float32.
        sigma (float): Standard deviation of the Gaussian noise, measured in range [0, 255].
            Default: 10.
        clip (bool): If True, clips output values to [0, 1] range. Default: True.
        rounds (bool): If True, rounds output values to nearest multiple of 1/255.
            Default: False.
        gray_noise (bool): If True, adds same grayscale noise across all channels.
            If False, adds independent noise per channel. Default: False.

    Returns:
        ndarray: Noisy output image of same shape as input, range depends on clip/rounds params.
    """
    noise = generate_gaussian_noise(img, sigma, gray_noise)
    out = img + noise
    if clip and rounds:
        out = np.clip((out * 255.0).round(), 0, 255) / 255.
    elif clip:
        out = np.clip(out, 0, 1)
    elif rounds:
        out = (out * 255.0).round() / 255.
    return out


def generate_gaussian_noise_pt(img, sigma=10, gray_noise=0):
    """Generate Gaussian noise for PyTorch tensors.

    Args:
        img (Tensor): Input image tensor of shape (b, c, h, w), range [0, 1], float32.
        sigma (float or Tensor): Standard deviation of noise. If Tensor, should be of shape (b,).
            Measured in range [0, 255]. Default: 10.
        gray_noise (float or Tensor): If float in (0,1], probability of using grayscale noise.
            If Tensor, should be of shape (b,) with binary values indicating grayscale noise
            per batch item. Default: 0.

    Returns:
        Tensor: Generated noise tensor of same shape as input, range roughly [-sigma/255, sigma/255].
    """
    b, _, h, w = img.size()
    if not isinstance(sigma, (float, int)):
        sigma = sigma.view(img.size(0), 1, 1, 1)
    if isinstance(gray_noise, (float, int)):
        cal_gray_noise = gray_noise > 0
    else:
        gray_noise = gray_noise.view(b, 1, 1, 1)
        cal_gray_noise = torch.sum(gray_noise) > 0

    if cal_gray_noise:
        noise_gray = torch.randn(*img.size()[2:4], dtype=img.dtype, device=img.device) * sigma / 255.
        noise_gray = noise_gray.view(b, 1, h, w)

    # always calculate color noise
    noise = torch.randn(*img.size(), dtype=img.dtype, device=img.device) * sigma / 255.

    if cal_gray_noise:
        noise = noise * (1 - gray_noise) + noise_gray * gray_noise
    return noise


def add_gaussian_noise_pt(img, sigma=10, gray_noise=0, clip=True, rounds=False):
    """Add Gaussian noise to PyTorch tensor images.

    Args:
        img (Tensor): Input image tensor of shape (b, c, h, w), range [0, 1], float32.
        sigma (float or Tensor): Standard deviation of noise. If Tensor, should be of shape (b,).
            Measured in range [0, 255]. Default: 10.
        gray_noise (float or Tensor): If float in (0,1], probability of using grayscale noise.
            If Tensor, should be of shape (b,) with binary values indicating grayscale noise
            per batch item. Default: 0.
        clip (bool): If True, clips output values to [0, 1] range. Default: True.
        rounds (bool): If True, rounds output values to nearest multiple of 1/255.
            Default: False.

    Returns:
        Tensor: Noisy output tensor of same shape as input, range depends on clip/rounds params.
    """
    noise = generate_gaussian_noise_pt(img, sigma, gray_noise)
